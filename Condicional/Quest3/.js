// 3) Faça um algoritmo para receber um número qualquer e informar na tela se é par ou ímpar.

let n = prompt ('Digite um número')
let par = 'par'
let impar = 'ímpar'

if (n % 2 == 0) {
    document.write(par)

} else {
    document.write(impar)
}

// Foram declaradas as variáveis na linha 1, 2 e 3;
// Na linha 5 foi declarado a condição caso ela seja par;
// Na linha 8 é declarado o resultado caso ele seja ímpar.